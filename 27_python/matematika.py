def sudetis(a, b):
    """Funkcija, kuri grazina dvieju skaiciu suma."""
    return a + b

def atimtis(a, b):
    """Funkcija, kuri grazina dvieju skaiciu skirtuma."""
    return a - b

def daugyba(a, b):
    """Funkcija, kuri grazina dvieju skaiciu sandauga."""
    return a * b

def dalyba(a, b):
    """Funkcija, kuri grazina dvieju skaiciu dalyba."""
    if b == 0:
        return "Dalyba is nulio negalima!"
    return a / b
